// Current language — set during state load in state.js
var lang = 'zh';

function t(k, ...a) {
  const v = I18N[lang]?.[k] ?? I18N.en?.[k] ?? k;
  return typeof v === 'function' ? v(...a) : v;
}

function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach(el => el.textContent = t(el.dataset.i18n));
  document.getElementById('search-input').placeholder = t('search_ph');
  document.getElementById('cmd-input').placeholder = t('cmd_ph');
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';
  const fabKeys = { 'fab-add':'fab_add', 'fab-settings':'fab_settings', 'fab-theme':'fab_theme', 'fab-pure':'fab_pure' };
  Object.entries(fabKeys).forEach(([id, key]) => {
    const tip = document.querySelector(`#${id} .fab-tooltip`);
    if (tip) tip.textContent = t(key);
  });
  const pe = document.getElementById('pure-esc');
  if (pe) pe.textContent = t('pure_esc_hint');
}
